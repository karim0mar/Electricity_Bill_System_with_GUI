import java.util.List;
import java.util.Map;

public class Tariff {
    private String type;  // Can be "fixed", "tiered", "time_of_use"
    private double rate;  // For "fixed" tariff
    private List<Tier> tiers;  // For "tiered" tariff
    private Map<String, Double> rates;  // For "time_of_use" tariff

    // Getters and Setters
    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public double getRate() {
        return rate;
    }

    public void setRate(double rate) {
        this.rate = rate;
    }

    public List<Tier> getTiers() {
        return tiers;
    }

    public void setTiers(List<Tier> tiers) {
        this.tiers = tiers;
    }

    public Map<String, Double> getRates() {
        return rates;
    }

    public void setRates(Map<String, Double> rates) {
        this.rates = rates;
    }

    // Inner class to represent a tier in the tiered tariff
    public static class Tier {
        private int limit;
        private double rate;

        public int getLimit() {
            return limit;
        }

        public void setLimit(int limit) {
            this.limit = limit;
        }

        public double getRate() {
            return rate;
        }

        public void setRate(double rate) {
            this.rate = rate;
        }
    }
}
